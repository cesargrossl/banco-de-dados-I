-- #########################################################################
-- 1. CRIAR DATABASE
-- #########################################################################

CREATE DATABASE IF NOT EXISTS escola;



-- #########################################################################
-- 2. SELECIONAR DATABASE
-- #########################################################################

USE escola;



-- #########################################################################
-- 3. CRIAR TABELA
-- #########################################################################
-- A tabela aluno será utilizada nos exemplos.
-- #########################################################################

CREATE TABLE IF NOT EXISTS aluno (

    id_aluno INT AUTO_INCREMENT PRIMARY KEY,

    nome VARCHAR(100) NOT NULL,

    email VARCHAR(150),

    idade INT,

    cidade VARCHAR(100)

);



-- #########################################################################
-- 4. INSERIR UM REGISTRO
-- #########################################################################
-- INSERT INTO adiciona novos dados na tabela.
-- #########################################################################

INSERT INTO aluno
(nome, email, idade, cidade)

VALUES

('Fulano de Tal', 'fulano@email.com', 20, 'Joinville');



-- #########################################################################
-- 5. INSERIR VÁRIOS REGISTROS
-- #########################################################################
-- É possível inserir vários registros ao mesmo tempo.
-- #########################################################################

INSERT INTO aluno
(nome, email, idade, cidade)

VALUES

('Ciclano da Silva', 'ciclano@email.com', 22, 'Blumenau'),
('Beltrano dos Santos', 'beltrano@email.com', 19, 'Curitiba'),
('João da Silva', 'joao@email.com', 25, 'Florianópolis');



-- #########################################################################
-- 6. CONSULTAR TODOS OS DADOS
-- #########################################################################
-- SELECT * exibe todos os registros da tabela.
-- #########################################################################

SELECT * FROM aluno;



-- #########################################################################
-- 7. CONSULTAR CAMPOS ESPECÍFICOS
-- #########################################################################
-- Exibe apenas os campos nome e cidade.
-- #########################################################################

SELECT nome, cidade
FROM aluno;



-- #########################################################################
-- 8. FILTRAR REGISTROS
-- #########################################################################
-- WHERE permite buscar registros específicos.
-- #########################################################################

SELECT *
FROM aluno
WHERE cidade = 'Joinville';



-- #########################################################################
-- 9. ALTERAR UM REGISTRO
-- #########################################################################
-- UPDATE altera dados existentes na tabela.
--
-- IMPORTANTE:
-- Sempre utilizar WHERE para evitar alterar todos os registros.
-- #########################################################################

UPDATE aluno
SET cidade = 'São Bento do Sul'
WHERE id_aluno = 1;



-- #########################################################################
-- 10. ALTERAR MAIS DE UM CAMPO
-- #########################################################################
-- É possível alterar vários campos no mesmo comando.
-- #########################################################################

UPDATE aluno
SET
    nome = 'Ciclano Oliveira',
    idade = 23
WHERE id_aluno = 2;



-- #########################################################################
-- 11. CONSULTAR DADOS ORDENADOS
-- #########################################################################
-- ORDER BY organiza os resultados.
-- #########################################################################

SELECT *
FROM aluno
ORDER BY nome ASC;



-- #########################################################################
-- 12. CONSULTAR COM LIMITE DE RESULTADOS
-- #########################################################################
-- LIMIT define quantos registros serão exibidos.
-- #########################################################################

SELECT *
FROM aluno
LIMIT 2;



-- #########################################################################
-- 13. EXCLUIR UM REGISTRO
-- #########################################################################
-- DELETE remove dados da tabela.
--
-- IMPORTANTE:
-- Sempre utilizar WHERE.
-- #########################################################################

DELETE FROM aluno
WHERE id_aluno = 4;



-- #########################################################################
-- 14. CONSULTAR DADOS APÓS DELETE
-- #########################################################################

SELECT * FROM aluno;



-- #########################################################################
-- 15. INSERIR NOVO REGISTRO
-- #########################################################################

INSERT INTO aluno
(nome, email, idade, cidade)

VALUES

('Maria da Silva', 'maria@email.com', 21, 'Jaraguá do Sul');



-- #########################################################################
-- 16. CONSULTAR DADOS
-- #########################################################################

SELECT * FROM aluno;



-- #########################################################################
-- 17. TRUNCATE TABLE
-- #########################################################################
-- Remove TODOS os registros da tabela.
--
-- A estrutura da tabela continua existindo.
-- #########################################################################

TRUNCATE TABLE aluno;



-- #########################################################################
-- 18. CONSULTAR APÓS TRUNCATE
-- #########################################################################
-- A tabela estará vazia.
-- #########################################################################

SELECT * FROM aluno;